public class OperatorTernary {
    public static void main(String[] args) {
        boolean suka = true;
        String jawaban;

        jawaban = suka ? "iya" : "tidak";

        System.out.println(jawaban);

        // Contoh lain ternary
        int x = 5;
        int hasil = x == 5 ? 10 : 20;
        System.out.println(hasil);
    }
}
